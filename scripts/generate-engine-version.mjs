#!/usr/bin/env node
// Computes ENGINE_VERSION (#251) as a content hash of every non-test source
// file under packages/engine/src and packages/content/src — the two packages
// that determine how a `GameConfig` plays out and replays. Hand-maintaining a
// version string was tried first (it sat at '0.0.1' since repo creation
// despite dozens of engine/content changes) and left the replay version guard
// permanently a no-op, since the value the guard compares never actually
// changed. A hash of the real inputs changes exactly when they do.
//
// Usage: `node scripts/generate-engine-version.mjs` regenerates
// packages/shared/src/engineVersion.generated.ts. Run it whenever
// packages/engine/src or packages/content/src changes; the test at
// apps/web/src/multiplayer/engineVersion.test.ts fails CI if you forget.

import { createHash } from 'node:crypto'
import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const REPO_ROOT = path.resolve(fileURLToPath(new URL('.', import.meta.url)), '..')
const HASHED_DIRS = ['packages/engine/src', 'packages/content/src']
const GENERATED_FILE = 'packages/shared/src/engineVersion.generated.ts'

function collectFiles(dir) {
  const out = []
  for (const entry of readdirSync(dir).sort()) {
    const full = path.join(dir, entry)
    const stat = statSync(full)
    if (stat.isDirectory()) {
      out.push(...collectFiles(full))
    } else if (entry.endsWith('.ts') && !entry.endsWith('.test.ts')) {
      out.push(full)
    }
  }
  return out
}

/** Hex digest of every hashed file's relative path and content, in sorted order. */
export function computeEngineVersion(repoRoot = REPO_ROOT) {
  const hash = createHash('sha256')
  const files = HASHED_DIRS.flatMap((dir) => collectFiles(path.join(repoRoot, dir))).sort()
  for (const file of files) {
    hash.update(path.relative(repoRoot, file).split(path.sep).join('/'))
    hash.update('\0')
    hash.update(readFileSync(file))
    hash.update('\0')
  }
  return hash.digest('hex').slice(0, 16)
}

function main() {
  const version = computeEngineVersion()
  const outPath = path.join(REPO_ROOT, GENERATED_FILE)
  writeFileSync(
    outPath,
    `// GENERATED by scripts/generate-engine-version.mjs — do not hand-edit.
// Regenerate after any packages/engine/src or packages/content/src change:
//   node scripts/generate-engine-version.mjs
export const ENGINE_VERSION = '${version}'
`,
  )
  console.log(`Wrote ${GENERATED_FILE}: ENGINE_VERSION = '${version}'`)
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main()
}
